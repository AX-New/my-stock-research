SYSTEM_ROLE = """你是一位专业的A股量化分析师。你的任务是基于给定的投资策略和当前市场环境，从全A股中筛选出最值得看好和最不看好的股票。你有数据库访问能力，可以直接查询股票行情、财务、资金流等数据。"""

STRATEGIES = """
## 9种选股策略

### 策略1：价值低估（value）
**投资哲学**: 市场并非总是有效的。当一家公司的股价低于其内在价值时，就存在投资机会。核心是"安全边际"。
**分析框架**:
1. 估值水平判断：综合PE、PB、PS、股息率，判断股价相对于盈利、资产、销售收入是否便宜。低PE可能是盈利即将下滑的信号，不能只看单一指标。
2. 盈利质量验证：关注经营现金流与净利润的匹配关系，关注扣非净利润，排除一次性收益。
3. 资产质量评估：关注资产负债率、有息负债、商誉占比。高负债低估值可能是"价值陷阱"。
4. 分红意愿：持续稳定分红是盈利真实性的佐证。
5. 历史估值分位：将当前估值与自身历史对比，判断是否处于历史低位。
**A股注意**: 大量"价值陷阱"（国企低PE低PB但不涨）；周期股盈利顶峰PE最低反而危险；需结合行业对比。
**重点指标方向**: PE(TTM)、PB、PS(TTM)、股息率、ROE、扣非净利润、经营现金流/净利润比、资产负债率

### 策略2：高成长（growth）
**投资哲学**: 找到业绩高速增长的公司。持续高增长会消化估值，关键是判断增长的质量和可持续性。
**分析框架**:
1. 增速验证：同时关注营收增速和净利润增速，营收增长是基础。
2. 增长质量：区分真增长和伪增长（低基数、并购、补贴），关注扣非净利润增速。
3. 增长加速度：季度营收环比加速说明增长在提速，环比减速说明拐点可能临近。
4. 盈利能力配合：ROE稳定或提升说明增长有效率；ROE下降说明"烧钱换增长"。
5. 估值与增长平衡：PEG（市盈率/增长率）是成长股最重要的估值工具。
**A股注意**: 成长股容易被过度炒作；警惕"增速断崖"；季度数据比年度更灵敏。
**重点指标方向**: 营收同比增速、净利润同比增速、扣非净利润增速、单季环比、PEG、ROE趋势

### 策略3：财务反转（turnaround）
**投资哲学**: 寻找基本面正在从差变好的公司。市场对"变好"反应滞后，预期差就是机会。核心参考Piotroski F-Score。
**分析框架**:
1. 盈利改善信号：ROA/ROE从负转正或低位回升，变化量比绝对值更重要。
2. 现金流改善：经营现金流从负转正，现金流改善是盈利改善的先行指标。
3. 杠杆下降：资产负债率同比下降，财务风险减小。
4. 运营效率提升：毛利率提升、资产周转率提升、流动比率提升。
5. 综合评分：多个维度同时改善是强信号，单一维度改善需谨慎。
**A股注意**: "假反转"多（卖资产、补贴）；关注连续2季度以上改善趋势。
**重点指标方向**: ROA变化、经营现金流变化、资产负债率变化、毛利率变化、资产周转率变化

### 策略4：技术趋势（trend）
**投资哲学**: 趋势一旦形成就有惯性。A股散户占比高，动量效应显著。不预测顶底，只做右侧。
**分析框架**:
1. 均线排列判断：MA5>MA10>MA20>MA60且斜率向上是最强多头形态。
2. 量价关系验证：放量上涨说明资金持续买入；缩量上涨趋势可能衰竭。
3. 趋势强度：股价站稳中期均线上方且回踩不破是延续信号。
4. 超买超卖：换手率、RSI判断短期是否过热。
5. 形态识别：突破盘整、回踩确认支撑、底部放量反转。
**A股注意**: 短期动量有效（2-4周）但月频动量不显著；默认参数效果最佳不要调参。
**重点指标方向**: MA排列状态、成交量与均量对比、换手率、近期涨跌幅、RSI

### 策略5：MACD信号（macd）
**投资哲学**: MACD是A股经实证验证最稳健的技术指标，融合趋势跟踪和动能判断。
**分析框架**:
1. 金叉/死叉信号：DIF上穿DEA为金叉（买入），下穿为死叉（卖出），需结合位置。
2. 零轴位置：零轴上方金叉远比下方可靠。零轴上二次金叉是高胜率信号。
3. MACD柱动能：红柱放大=上涨动能增强；红柱缩短=动能衰减。柱变化领先价格。
4. 背离信号：顶背离是见顶预警，底背离是见底信号，A股有效性较高。
5. 与量能配合：金叉配合放量可靠性显著提升。
**A股注意**: 默认参数(12,26,9)效果最佳；零轴上二次金叉20日上涨概率约71%；震荡市假信号多。
**重点指标方向**: DIF/DEA值及方向、MACD柱变化、零轴位置、金叉死叉状态、背离

### 策略6：综合精选（comprehensive）
**投资哲学**: 不限定单一维度，根据当前市场环境自主决定最优选股方法。"因地制宜"。
**分析框架**:
1. 市场环境研判：根据市场统计数据判断市场阶段（上升/下降/震荡）。
2. 策略适配选择：自主决定侧重哪些维度，可组合使用其他策略方法。
3. 多维度交叉验证：多维度共振的信号更可靠。
4. 风险收益平衡：高收益高风险不一定优于稳健选择。
5. 独立思考：可提出被其他框架忽略的机会或风险。
**A股注意**: 风格切换频繁（3-6个月）；政策影响巨大；有效因子可能与历史不同。
**重点指标方向**: 由模型自主决定，需在分析理由中说明。

### 策略7：市场热点（hotspot）
**投资哲学**: A股主题驱动特征极强。热点形成时相关个股短期内获得大量资金关注。核心是识别主题方向。
**分析框架**:
1. 热点识别：通过行业板块涨幅排名、涨停原因归类、成交额集中度识别主线方向。
2. 热点强度判断：板块内涨停数量、成交额占比、连续上涨天数。
3. 龙头与跟风区分：龙头最先涨停、封板坚决；跟风退潮时跌幅更大。
4. 热点生命周期：启动期→发酵期→高潮期→退潮期，最佳介入发酵期。
5. 产业逻辑验证：有产业逻辑支撑的热点持续性强，纯概念炒作退潮快。
**A股注意**: 热点通常持续1-3周；政策驱动的最强最持久；机构买入比游资更有持续性。
**重点指标方向**: 行业涨幅排名、板块涨停数量、成交额占比、龙虎榜数据

### 策略8：资金面共振（capital_flow）
**投资哲学**: A股散户占比高，主力资金动向对短中期股价影响显著。资金面信号可作为其他基本面/技术面信号的"确认项"，提高择时胜率。
**分析框架**:
1. 主力资金流向：近5日主力净流入持续为正，说明机构/大户在持续建仓。单日大额流入可能是游资短炒，持续性更重要。
2. 量价同步验证：上涨放量、下跌缩量是健康的资金结构。放量滞涨或缩量阴跌是危险信号。
3. 个股活跃度：换手率处于合理区间（5%-15%为活跃区）。过低说明无人关注，过高说明短期过热或筹码松动。
4. 板块资金共振：个股所在板块整体获得资金流入，说明是板块行情而非个股炒作，持续性更强。
5. 北向资金参考：北向资金持续净买入的标的通常有基本面支撑，但需注意北向也会"砸盘"。
**A股注意**: 资金面信号不能单独构成买入理由，必须与基本面或技术面共振；游资炒作标的资金来得快去得也快；关注持续性而非单日。
**重点指标方向**: 近5日主力净流入、量价配合度、换手率、板块资金流向、北向资金动向

### 策略9：低波红利防御（dividend_defense）
**投资哲学**: 在市场不确定性高或下行趋势中，高股息+低波动的标的提供"安全垫"。核心是分红的可持续性和估值的合理性，而非单纯追求高股息率。
**分析框架**:
1. 股息率评估：近3年平均股息率>3%，且分红比例稳定或提升。单年高股息可能是特殊分红，不可持续。
2. 分红可持续性：经营现金流能覆盖分红支出，ROE稳定在10%以上，业务模式成熟（公用事业/交运/银行等）。
3. 估值安全：PB偏低（<1视为破净安全区）、PE处于历史低位区间，避免"高息陷阱"。
4. 低波动特征：近60日/120日波动率低于市场中位数，最大回撤幅度小，持有体验好。
5. 防御适配：弱市/震荡市中跑赢大盘，市场恐慌时跌幅显著小于指数。
**A股注意**: 高股息不等于好股票，需验证盈利真实性；银行/公用事业/交运是传统红利板块但需区分个股；牛市中红利策略通常跑输成长策略。
**重点指标方向**: 股息率(近3年平均)、分红比例、PB、波动率(60日/120日)、最大回撤、经营现金流/分红覆盖率
"""

TASK_INSTRUCTION = """
## 任务指令

### 执行流程

**第一步：市场环境研判**
阅读上方的市场统计数据，给出结构化判断：
- market_trend: up/down/sideways（市场趋势方向）
- market_capital: strong/weak/neutral（资金面强弱）
- market_sentiment: optimistic/pessimistic/neutral（市场情绪）
- market_view: 用2-3句话概括当前市场状态

**第二步：策略筛选**
从上方9种策略中选择5种执行。说明放弃了哪4种策略及原因。

**第三步：逐策略选股**
对选中的每种策略，通过查询数据库完成选股：
- 选出10只最看好的股票（direction=long）：最符合该策略方向，上涨概率最大
- 选出10只最不看好的股票（direction=short）：最违背该策略方向，下跌概率最大
- 为每只股票给出评级、评分、买入价、目标价、分析理由

### 选股要求
- 排除ST、*ST、停牌、上市不满1年的股票
- 排除北交所股票
- 同一只股票可以出现在不同策略中，但同一策略内不可重复
- 做多组和做空组不能有重叠（同一策略内）
- 买入价使用最新收盘价
- 目标价是你认为未来20个交易日内可能达到的价格

### 输出要求
将结果逐条INSERT到数据库表 `llm_stock_pick`，字段如下：

```sql
INSERT INTO llm_stock_pick (
    eval_date, model_name, methodology,
    ts_code, stock_name, direction, rating, score,
    buy_price, target_price, analysis,
    market_trend, market_capital, market_sentiment, market_view,
    dropped_strategies, drop_reason
) VALUES (
    '{eval_date}', '{model_name}', '策略标识',
    '股票代码', '股票名称', 'long/short', 'buy/hold/sell', 评分,
    买入价, 目标价, '分析理由(100字内)',
    'up/down/sideways', 'strong/weak/neutral', 'optimistic/pessimistic/neutral', '市场研判概述',
    '放弃的策略逗号分隔', '放弃原因'
);
```

**methodology取值**: value / growth / turnaround / trend / macd / comprehensive / hotspot / capital_flow / dividend_defense
**market_trend/market_capital/market_sentiment/market_view/dropped_strategies/drop_reason**: 每条记录都填写相同的值
"""


def build_prompt(eval_date: str = None, model_name: str = "claude") -> str:
    """构建完整的选股提示词"""
    market_data = format_market_stats(eval_date)
    if not eval_date:
        from la.scripts.market_stats import get_latest_trade_date
        eval_date = get_latest_trade_date()

    task = TASK_INSTRUCTION.replace("{eval_date}", eval_date or "").replace("{model_name}", model_name)

    parts = [
        SYSTEM_ROLE.strip(),
        "",
        STRATEGIES.strip(),
        "",
        market_data,
        "",
        task.strip(),
    ]
    return "\n\n".join(parts)


if __name__ == "__main__":
    import argparse
    parser = argparse.ArgumentParser(description="生成大模型选股提示词")
    parser.add_argument("--date", help="评审日期 YYYYMMDD，默认最新交易日")
    parser.add_argument("--model", default="claude", help="模型名称: claude/doubao/gemini")
    parser.add_argument("--output", help="输出文件路径，默认打印到终端")
    args = parser.parse_args()

    prompt = build_prompt(args.date, args.model)

    if args.output:
        with open(args.output, "w", encoding="utf-8") as f:
            f.write(prompt)
        print(f"已输出到 {args.output}")
    else:
        print(prompt)
